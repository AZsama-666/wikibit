// 风险曲线数据（30天）- 每个交易所30天数据
function generateRiskSeries(baseScore: number, volatility: number = 3): RiskPoint[] {
  const startDate = new Date(2024, 0, 1);
  return Array.from({ length: 30 }, (_, i) => {
    const date = new Date(startDate);
    date.setDate(startDate.getDate() + i);
    // 围绕baseScore波动，添加一些趋势
    const trend = Math.sin(i / 8) * 2;
    const random = (Math.random() - 0.5) * volatility;
    return {
      date: date.toISOString().split("T")[0],
      score: Math.max(0, Math.min(100, baseScore + trend + random)),
    };
  });
}

